#include "libft.h"

char	*ft_strjoin(const char *s1, const char *s2)
{
	char	*dest;

	dest = (char *)malloc(ft_strlen(s1) + ft_strlen(s2) + 1);
	if (!dest)
		return (NULL);
	ft_memcpy(dest, s1, ft_strlen(s1));
	ft_memcpy(dest + ft_strlen(s1), s2, ft_strlen(s2) + 1);
	return (dest);
}
